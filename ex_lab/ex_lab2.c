#include <stdio.h>
#include <stdlib.h>

int main()
{
    char s = '1';
    printf("%c\n", s);
    printf("%c\n", s + 1);
    printf("%c\n", s + 2);
    printf("%c\n", s + 3);
    printf("%c\n", s + 4);
    printf("\n\n");

    s = 'a';
    printf("%c\n", s);
    printf("%c\n", s + 1);
    printf("%c\n", s + 2);
    printf("%c\n", s + 3);
    printf("%c\n", s + 4);
    printf("\n\n");

    s = 'A';
    printf("%c\n", s);
    printf("%c\n", s + 1);
    printf("%c\n", s + 2);
    printf("%c\n", s + 3);
    printf("%c\n", s + 4);
    printf("\n\n");

    return 0;
}